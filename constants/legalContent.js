export function getLegalContent(type, lang) {
  const l = (lang ?? 'es').split('-')[0];
  if (type === 'terms') {
    if (l === 'en') return TERMS_CONTENT_EN;
    if (l === 'nb') return TERMS_CONTENT_NB;
    return TERMS_CONTENT_ES;
  }
  if (l === 'en') return PRIVACY_CONTENT_EN;
  if (l === 'nb') return PRIVACY_CONTENT_NB;
  return PRIVACY_CONTENT_ES;
}
